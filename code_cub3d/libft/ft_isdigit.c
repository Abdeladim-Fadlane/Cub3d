int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}

int	ft_isdigit2(int c)
{
	if ((c >= '0' && c <= '9') || c == ' ' || c == '\t')
		return (1);
	else
		return (0);
}
